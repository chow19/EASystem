package com.EaSystem.beans;

import java.util.Date;

public class CalendarofWeek {
	int yearName;
	int weekName;
	Date dayofMonday;
	Date dayofTuesday;
	Date dayofWednesday;
	Date dayofThursday;
	Date dayofFriday;
	Date dayofSaturday;
	Date dayofSunday;
	
	public int getYearName() {
		return yearName;
	}
	public void setYearName(int yearName) {
		this.yearName = yearName;
	}
	public int getWeekName() {
		return weekName;
	}
	public void setWeekName(int weekName) {
		this.weekName = weekName;
	}
	public Date getDayofMonday() {
		return dayofMonday;
	}
	public void setDayofMonday(Date dayofMonday) {
		this.dayofMonday = dayofMonday;
	}
	public Date getDayofTuesday() {
		return dayofTuesday;
	}
	public void setDayofTuesday(Date dayofTuesday) {
		this.dayofTuesday = dayofTuesday;
	}
	public Date getDayofWednesday() {
		return dayofWednesday;
	}
	public void setDayofWednesday(Date dayofWednesday) {
		this.dayofWednesday = dayofWednesday;
	}
	public Date getDayofThursday() {
		return dayofThursday;
	}
	public void setDayofThursday(Date dayofThursday) {
		this.dayofThursday = dayofThursday;
	}
	public Date getDayofFriday() {
		return dayofFriday;
	}
	public void setDayofFriday(Date dayofFriday) {
		this.dayofFriday = dayofFriday;
	}
	public Date getDayofSaturday() {
		return dayofSaturday;
	}
	public void setDayofSaturday(Date dayofSaturday) {
		this.dayofSaturday = dayofSaturday;
	}
	public Date getDayofSunday() {
		return dayofSunday;
	}
	public void setDayofSunday(Date dayofSunday) {
		this.dayofSunday = dayofSunday;
	}
}
