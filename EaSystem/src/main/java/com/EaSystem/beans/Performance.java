package com.EaSystem.beans;

public class Performance {
	int class_id;
	int student_id;
	String exam_method;
	int daily_achievement;
	int test_scores;
	int final_scores;
	int credit;
	public int getClass_id() {
		return class_id;
	}
	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}
	public int getStudent_id() {
		return student_id;
	}
	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}
	public String getExam_method() {
		return exam_method;
	}
	public void setExam_method(String exam_method) {
		this.exam_method = exam_method;
	}
	public int getDaily_achievement() {
		return daily_achievement;
	}
	public void setDaily_achievement(int daily_achievement) {
		this.daily_achievement = daily_achievement;
	}
	public int getTest_scores() {
		return test_scores;
	}
	public void setTest_scores(int test_scores) {
		this.test_scores = test_scores;
	}
	public int getFinal_scores() {
		return final_scores;
	}
	public void setFinal_scores(int final_scores) {
		this.final_scores = final_scores;
	}
	public int getCredit() {
		return credit;
	}
	public void setCredit(int credit) {
		this.credit = credit;
	}
}
